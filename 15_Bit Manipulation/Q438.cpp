/*
Q438.) Count number of bits to be flipped to convert A to B

INPUT:
2

10 20

20 25

OUTPUT:
Case #1: 4
Case #2: 3

*/

class Solution
{
public:
    // To count set bits
    int countSetBits(unsigned int n)
    {
        // We use a count variable
        unsigned int count = 0;
        while (n)
        {
            // AND operation of n and 1 gives us the rightmost bit.
            // counter variable is increased if we get 1 as the rightmost bit.
            count += n & 1;

            // Right Shift n by 1 to remove the rightmost bit.
            n >>= 1;
        }
        return count;
    }

    // Function to find number of bits needed to be flipped to convert A to B
    int countBitsFlip(int a, int b)
    {

        int ans = 0;
        // XOR operation gives set bits only when there are dis-similar bits.
        // We store the value of XOR operation of a and b.
        ans = a ^ b;

        // returning the number of set bits in resultant.
        return countSetBits(ans);
    }
};

void solve()
{
    int a, b;
    cin >> a >> b; // input a and b

    Solution ob;
    cout << ob.countBitsFlip(a, b) << endl;
}

