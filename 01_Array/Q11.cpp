/*
Q11.) find duplicate in an array of N + 1 Integers

INPUT:
3

5
1 3 4 2 2

8
1 5 7 9 3 5 8 5

5
3 2 3 4 2

OUTPUT:
Case #1: 2
Case #2: 5
Case #3: 3

*/

class Solution
{
public:
    int findDuplicate(vector<int> &nums)
    {
        int duplicate = -1;
        for (int i = 0; i < nums.size(); i++)
        {
            // Making curr positive, as index can't be negative
            int cur = abs(nums[i]);
            // If number is negative, It has been passed before
            if (nums[cur] < 0)
            {
                // Found the duplicate number
                duplicate = cur;
                break;
            }
            // Making every element negative
            nums[cur] *= -1;
        }
        return duplicate;
    }
};

void solve()
{

    int n;
    cin >> n;
    vector<int> v(n);

    for (int i = 0; i < n; i++)
    {
        cin >> v[i];
    }

    Solution ob;
    cout << ob.findDuplicate(v) << endl;
}

