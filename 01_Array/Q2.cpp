/*
Q2.) Find the maximum and minimum element in an array

INPUT:
2

4
1 2 3 4

8
67 65 23 53 13 16 98 33

OUTPUT:
Case #1: 1 4
Case #2: 13 98

*/

pair<long long, long long> getMinMax(long long a[], int n)
{
    // Declaring mx = Minimum value && mn = Maximum value
    ll mn = INT_MAX, mx = INT_MIN;

    for (int i = 0; i < n; i++)
    {
        // Finding minimum value between a[i] & prev mn value
        mn = min(a[i], mn);
        // Finding maximum value between a[i] & prev mn value
        mx = max(a[i], mx);
    }

    return {mn, mx};
}

void solve()
{
    int n;
    cin >> n;
    ll a[n];
    for (int i = 0; i < n; i++)
        cin >> a[i];

    pair<ll, ll> pp = getMinMax(a, n);

    cout << pp.first << " " << pp.second << endl;
}

