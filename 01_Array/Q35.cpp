/*
Q35.) Median of 2 sorted arrays of equal size

INPUT:
3

5
67 78 89 90 100
30 34 56 67 79

5
1 12 15 26 38
2 13 17 30 45

4
1 2 3 6
4 6 8 10

OUTPUT:
Case #1: 72
Case #2: 16
Case #3: 5

*/

class Solution
{
public:
    int getMedian(vector<int> ar1, vector<int> ar2, int n)
    {
        int j = 0;
        int i = n - 1;
        // Take the union of the input arrays ar1[] and ar2[].
        while (ar1[i] > ar2[j] && j < n && i > -1)
        {
            swap(ar1[i--], ar2[j++]);
        }
        // Sort ar1[] and ar2[] respectively.
        sort(all(ar1));
        sort(all(ar2));
        // The median will be the last element of ar1[]
        // + the first element of ar2[] divided by 2.
        int ans = (ar1[n - 1] + ar2[0]) / 2;

        return ans;
    }
};

void solve()
{
    int n;
    cin >> n;
    vector<int> a(n);
    vector<int> b(n);
    for (int i = 0; i < n; i++)
    {
        cin >> a[i];
    }

    for (int i = 0; i < n; i++)
    {
        cin >> b[i];
    }

    Solution ob;
    cout << ob.getMedian(a, b, n) << endl;
}

