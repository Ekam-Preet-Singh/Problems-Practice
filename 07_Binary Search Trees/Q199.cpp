/*
Q199.) Find inorder successor and inorder predecessor in a BST

INPUT:
2

4
78 34 L 78 97 R 34 12 L 34 45 R
34

5
78 80 R 78 24 L 24 18 L 24 55 R 80 85 R
40

OUTPUT:
Case #1: 12 45
Case #2: 24 55

*/

// BST Node
struct Node
{
    int key;
    struct Node *left;
    struct Node *right;

    Node(int x)
    {
        key = x;
        left = NULL;
        right = NULL;
    }
};

void insert(struct Node *root, int n1, int n2, char lr)
{
    if (root == NULL)
        return;
    if (root->key == n1)
    {
        switch (lr)
        {
        case 'L':
            root->left = new Node(n2);
            break;
        case 'R':
            root->right = new Node(n2);
            break;
        }
    }
    else
    {
        insert(root->left, n1, n2, lr);
        insert(root->right, n1, n2, lr);
    }
}

int key = 0;

// This function finds predecessor and successor of key in BST.
// It sets pre and suc as predecessor and successor respectively
void findPreSuc(Node *root, Node *&pre, Node *&suc, int key)
{

    // Base case
    if (root == NULL)
        return;

    // If key is present at root
    if (root->key == key)
    {
        // the maximum value in left subtree is predecessor
        if (root->left != NULL)
        {
            Node *tmp = root->left;
            while (tmp->right)
                tmp = tmp->right;
            pre = tmp;
        }

        // the minimum value in right subtree is successor
        if (root->right != NULL)
        {
            Node *tmp = root->right;
            while (tmp->left)
                tmp = tmp->left;
            suc = tmp;
        }
        return;
    }

    // If key is smaller than root's key, go to left subtree
    if (root->key > key)
    {
        suc = root;
        findPreSuc(root->left, pre, suc, key);
    }
    else // go to right subtree
    {
        pre = root;
        findPreSuc(root->right, pre, suc, key);
    }
}

void solve()
{
    int n;
    cin >> n;
    struct Node *root = NULL;
    Node *pre = NULL;
    Node *suc = NULL;
    while (n--)
    {
        char lr;
        int n1, n2;
        cin >> n1 >> n2;
        cin >> lr;
        if (root == NULL)
        {
            root = new Node(n1);
            switch (lr)
            {
            case 'L':
                root->left = new Node(n2);
                break;
            case 'R':
                root->right = new Node(n2);
                break;
            }
        }
        else
        {
            insert(root, n1, n2, lr);
        }
    }
    cin >> key;
    findPreSuc(root, pre, suc, key);
    if (pre != NULL)
        cout << pre->key;
    else
        cout << "-1";

    if (suc != NULL)
        cout << " " << suc->key << endl;
    else
        cout << " -1" << endl;
}

