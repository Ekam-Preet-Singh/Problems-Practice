/*
Q193.) Kth Ancestor of node in a Binary tree

INPUT:
2

1 2 3 4 5
2 5

1 2 3 4 5 N N
2 3

OUTPUT:
Case #1: Kth ancestor is: 1
Case #2: -1

*/

// A Binary Tree Node
struct Node
{
    int data;
    struct Node *left, *right;
};

// temporary node to keep track of Node returned
// from previous recursive call during backtrack
Node *temp = NULL;

// recursive function to calculate Kth ancestor
Node *kthAncestorDFS(Node *root, int node, int &k)
{
    // Base case
    if (!root)
    {
        return NULL;
    }

    if (root->data == node || (temp = kthAncestorDFS(root->left, node, k)) || (temp = kthAncestorDFS(root->right, node, k)))
    {
        if (k > 0)
        {
            k--;
        }

        else if (k == 0)
        {
            // print the kth ancestor
            cout << "Kth ancestor is: " << root->data;

            // return NULL to stop further backtracking
            return NULL;
        }

        // return current node to previous call
        return root;
    }
}

// Utility function to create a new tree node
Node *newNode(int data)
{
    Node *temp = new Node;
    temp->data = data;
    temp->left = temp->right = NULL;
    return temp;
}

// Function to Build Tree
Node *buildTree(string str)
{
    // Corner Case
    if (str.length() == 0 || str[0] == 'N')
        return NULL;

    // Creating vector of strings from input
    // string after spliting by space
    vector<string> ip;

    istringstream iss(str);
    for (string str; iss >> str;)
        ip.push_back(str);

    // Create the root of the tree
    Node *root = newNode(stoi(ip[0]));

    // Push the root to the queue
    queue<Node *> queue;
    queue.push(root);

    // Starting from the second element
    int i = 1;
    while (!queue.empty() && i < ip.size())
    {

        // Get and remove the front of the queue
        Node *currNode = queue.front();
        queue.pop();

        // Get the current node's value from the string
        string currVal = ip[i];

        // If the left child is not null
        if (currVal != "N")
        {

            // Create the left child for the current node
            currNode->left = newNode(stoi(currVal));

            // Push it to the queue
            queue.push(currNode->left);
        }

        // For the right child
        i++;
        if (i >= ip.size())
            break;
        currVal = ip[i];

        // If the right child is not null
        if (currVal != "N")
        {

            // Create the right child for the current node
            currNode->right = newNode(stoi(currVal));

            // Push it to the queue
            queue.push(currNode->right);
        }
        i++;
    }

    return root;
}

void solve()
{
    string s;
    getline(cin, s);

    Node *root = buildTree(s);
    int k, n;
    cin >> k >> n;
    // print kth ancestor of given node
    Node *parent = kthAncestorDFS(root, n, k);

    // check if parent is not NULL, it means
    // there is no Kth ancestor of the node
    if (parent)
        cout << "-1";
}

