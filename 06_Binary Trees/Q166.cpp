/*
Q166.) Inorder Traversal of a tree both using recursion and Iteration

INPUT:
4

1 3 2

5 5 N 4 10 N 8 5 N 8 8 N 6

10 20 30 40 60 N N

5 3 6 2 4 N N

OUTPUT:
Case #1:
Recursive Inorder Traversal: 3 1 2
Iterative Inorder Traversal: 3 1 2
Case #2:
Recursive Inorder Traversal: 4 8 8 8 5 5 6 10 5
Iterative Inorder Traversal: 4 8 8 8 5 5 6 10 5
Case #3:
Recursive Inorder Traversal: 40 20 60 10 30
Iterative Inorder Traversal: 40 20 60 10 30
Case #4:
Recursive Inorder Traversal: 2 3 4 5 6
Iterative Inorder Traversal: 2 3 4 5 6

*/

struct Node
{
    int val;
    struct Node *left;
    struct Node *right;
};
Node *newNode(int val)
{
    Node *temp = new Node;
    temp->val = val;
    temp->left = NULL;
    temp->right = NULL;
    return temp;
}
Node *buildTree(string str)
{
    // Corner Case
    if (str.length() == 0 || str[0] == 'N')
        return NULL;

    // Creating vector of strings from input
    // string after spliting by space
    vector<string> ip;

    istringstream iss(str);
    for (string str; iss >> str;)
        ip.push_back(str);

    // Create the root of the tree
    Node *root = newNode(stoi(ip[0]));

    // Push the root to the queue
    queue<Node *> queue;
    queue.push(root);

    // Starting from the second element
    int i = 1;
    while (!queue.empty() && i < ip.size())
    {

        // Get and remove the front of the queue
        Node *currNode = queue.front();
        queue.pop();

        // Get the current node's value from the string
        string currVal = ip[i];

        // If the left child is not null
        if (currVal != "N")
        {

            // Create the left child for the current node
            currNode->left = newNode(stoi(currVal));

            // Push it to the queue
            queue.push(currNode->left);
        }

        // For the right child
        i++;
        if (i >= ip.size())
            break;
        currVal = ip[i];

        // If the right child is not null
        if (currVal != "N")
        {

            // Create the right child for the current node
            currNode->right = newNode(stoi(currVal));

            // Push it to the queue
            queue.push(currNode->right);
        }
        i++;
    }

    return root;
}

// Recursive function to perform inorder traversal on the tree
void inorderRecursive(Node *root)
{
    // return if the current node is empty
    if (root == nullptr)
    {
        return;
    }

    // Traverse the left subtree
    inorderRecursive(root->left);

    // Display the data part of the root (or current node)
    cout << root->val << " ";

    // Traverse the right subtree
    inorderRecursive(root->right);
}

// Iterative function to perform inorder traversal on the tree
void inorderIterative(Node *root)
{
    // create an empty stack
    stack<Node *> stack;

    // start from the root node (set current node to the root node)
    Node *curr = root;

    // if the current node is null and the stack is also empty, we are done
    while (!stack.empty() || curr != nullptr)
    {
        // if the current node exists, push it into the stack (defer it)
        // and move to its left child
        if (curr != nullptr)
        {
            stack.push(curr);
            curr = curr->left;
        }
        else
        {
            // otherwise, if the current node is null, pop an element from the stack,
            // print it, and finally set the current node to its right child
            curr = stack.top();
            stack.pop();
            cout << curr->val << " ";

            curr = curr->right;
        }
    }
}

void solve()
{
    string s;
    getline(cin, s);
    Node *root = buildTree(s);
    cout << "\nRecursive Inorder Traversal: ";
    inorderRecursive(root);
    cout << "\nIterative Inorder Traversal: ";
    inorderIterative(root);
}

